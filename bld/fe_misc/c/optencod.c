static void emitPrintf( unsigned depth, const char *msg, ... )
{
    va_list args;
    unsigned i;

    if( pfp != NULL ) {
        for( i = ( depth >> 1 ); i != 0; --i ) {
            fprintf( pfp, "\t" );
        }
        if( depth & 1 ) {
            fprintf( pfp, "    " );
        }
        va_start( args, msg );
        vfprintf( pfp, msg, args );
        va_end( args );
    }
}

static char *cvtOptionSpec( char *dst, const char *src, cvt_name cvt )
{
    char c;

    while( (c = *src++) != '\0' ) {
        if( c == '\\' ) {
            if( cvt == CVT_PATTERN ) {
                *dst++ = c;
            }
            c = *src++;
        } else if( cvt != CVT_USAGE ) {
            c = mytolower( c );
        }
        *dst++ = c;
    }
    *dst = '\0';
    return( dst );
}

static bool cmpOptPattern( const char *pattern1, const char *pattern2 )
{
    char    c1;
    char    c2;

    for( ;; ) {
        c1 = *pattern1++;
        c2 = *pattern2++;
        if( c1 == '\\' ) {
            if( c2 != '\\' ) {
                return( false );
            }
            c1 = *pattern1++;
            c2 = *pattern2++;
        } else {
            c1 = mytolower( c1 );
            c2 = mytolower( c2 );
        }
        if( c1 != c2 ) {
            return( false );
        }
        if( c1 == '\0' ) {
            break;
        }
    }
    return( true );
}

static bool cmpChainPattern( const char *pattern1, const char *pattern2, size_t pattern_len )
{
    size_t  i;
    char    c1;
    char    c2;

    for( i = 0; i < pattern_len; ++i ) {
        c1 = *pattern1++;
        c2 = *pattern2++;
        if( c1 == '\\' ) {
            if( c2 != '\\' ) {
                return( false );
            }
            c1 = *pattern1++;
            c2 = *pattern2++;
        } else {
            c1 = mytolower( c1 );
            c2 = mytolower( c2 );
        }
        if( c1 != c2 ) {
            return( false );
        }
    }
    return( true );
}

static void addTarget( const char *t )
{
    size_t len;
    TARGET *p;

    len = strlen( t );
    p = malloc( sizeof( *p ) + len );
    p->mask = nextTargetMask;
    p->next = targetList;
    targetList = p;
    strcpy( p->name, t );
    nextTargetMask <<= 1;
    if( nextTargetMask == 0 ) {
        fail( "too many targets defined\n" );
    }
}

static unsigned findTarget( char const *t )
{
    TARGET *p;

    for( p = targetList; p != NULL; p = p->next ) {
        if( strcmp( t, p->name ) == 0 ) {
            return( p->mask );
        }
    }
    return( 0 );
}

