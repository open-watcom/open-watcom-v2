static int mystricmp( const char *p1, const char *p2 )
{
    while( mytolower( *p1 ) == mytolower( *p2 ) ) {
        if( *p1 == '\0') {
            return( 0 );
        }
        p1++; p2++;
    }
    return( mytolower( *p1 ) - mytolower( *p2 ) );
}

