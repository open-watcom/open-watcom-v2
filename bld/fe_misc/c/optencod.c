static char *ReadIndirectFile( char *name )
/*****************************************/
{
    char        *str;
    FILE        *fp;
    size_t      len;
    char        ch;

    str = NULL;
    fp = fopen( name, "rb" );
    if( fp != NULL ) {
        fseek( fp, 0, SEEK_END );
        len = ftell( fp );
        fseek( fp, 0, SEEK_SET );
        str = malloc( len + 1 );
        fread( str, 1, len, fp );
        str[len] = '\0';
        fclose( fp );
        // go through characters changing \r, \n etc into ' '
        for( ; (ch = *str) != '\0'; str++ ) {
            if( ch == 0x1A ) {      // if end of file
                *str = '\0';        // - mark end of str
                break;
            }
            if( ch != ' '
              && myisspace( ch ) ) {
                *str = ' ';
            }
        }
    }
    return( str );
}

static char *getFileName( char *str, char *name )
/***********************************************/
{
    char        ch;

    SKIP_SPACES( str );
    if( *str == '\"' ) {
        str++;
        while( (ch = *str) != '\0' ) {
            str++;
            if( ch == '"' ) {
                break;
            }
            if( ch == '\\'
              && *str == '"' ) {
                ch = *str++;
            }
            *name++ = ch;
        }
    } else {
        while( *str != '\0'
          && !myisspace( *str ) ) {
            *name++ = *str++;
        }
    }
    *name = '\0';
    return( str );
}

static char *ProcessOption( char *s, char *option_start )
/*******************************************************/
{
    switch( *s++ ) {
    case 'c':
        optFlag.list_sep = ",";
        return( s );
    case 'i':
        optFlag.international = true;
        return( s );
    case 'l':
        if( *s++ == '=' ) {
            optFlag.lang = strtol( s, &s, 10 );
            return( s );
        }
        break;
    case 'n':
        if( *s == 'n' ) {
            s++;
            optFlag.line_term = "\\n";
        } else {
            optFlag.line_term = "\\0";
        }
        return( s );
    case 'p':
        optFlag.parser_only = true;
        return( s );
    case 'q':
        optFlag.quiet = true;
        return( s );
    case 'r':
        if( *s++ == 'c'
          && *s++ == '=' ) {
            optFlag.rc = true;
            SKIP_SPACES( s );
            optFlag.rc_macro = option_start;
            while( *s != '\0' ) {
                if( myisspace( *s ) ) {
                    s++;
                    break;
                }
                *option_start++ = *s++;
            }
            *option_start = '\0';
            return( s );
        }
        break;
    case 't':
        optFlag.report_missing_data = true;
        return( s );
    case 'u':
        if( *s == '=' ) {
            s = getFileName( s + 1, option_start );
            mfp = fopen( option_start, "wb" );
            if( mfp == NULL ) {
                fail( "cannot open '%s' for output", option_start );
            }
            return( s );
        } else if( *s++ == 't'
          && *s++ == 'f'
          && *s++ == '8' ) {
            optFlag.out_utf8 = true;
            return( s );
        }
        break;
    case 'x':
        if( *s == '=' ) {
            s++;
            SKIP_SPACES( s );
            optFlag.sid = option_start;
            while( *s != '\0' ) {
                if( myisspace( *s ) ) {
                    s++;
                    break;
                }
                *option_start++ = *s++;
            }
            *option_start = '\0';
            return( s );
        }
        break;
    }
    printf( "Unknown option: %s\n", option_start );
    return( NULL );
}

static void initOptions( void )
/*****************************/
{
     optFlag.line_term = "";
     optFlag.list_sep = "";
     optFlag.sid = "";
}

static bool ProcessOptions( char *str )
/*************************************/
{
    char        name[PATH_MAX];
    char        *fstr;
    bool        rc;
    char        *p;
    targmask    mask;

    rc = 0;
    fstr = NULL;
    name[0] = '\0';
    while( *str != '\0' ) {
        SKIP_SPACES( str );
        if( *str == '@' ) {
            str = getFileName( str + 1, name );
            str = getenv( name );
            if( str == NULL ) {
                str = fstr = ReadIndirectFile( name );
            }
        }
        if( *str == '\0' ) {
            continue;
        }
        if( *str == '-' ) {
            str = ProcessOption( str + 1, str );
            if( str == NULL ) {
                rc = true;
                break;
            }
        } else {  /* collect file name */
            switch( fno++ ) {
            case 0:
                str = getFileName( str, name );
                gfp = initFILE( name, "r" );
                break;
            case 1:
                str = getFileName( str, name );
                ofp = initFILE( name, "w+" );
                break;
            case 2:
                str = getFileName( str, name );
                pfp = initFILE( name, "w+" );
                break;
            case 3:
                str = getFileName( str, name );
                ufp = initFILE( name, "w+" );
                p = "any";
                if( (targetAnyMask = findTarget( p )) == 0 ) {
                    fail( "invalid target name '%s'\n", p );
                }
                p = "dbg";
                if( (targetDbgMask = findTarget( p )) == 0 ) {
                    fail( "invalid target name '%s'\n", p );
                }
                p = "unused";
                if( (targetUnusedMask = findTarget( p )) == 0 ) {
                    fail( "invalid target name '%s'\n", p );
                }
                targetMask |= targetAnyMask;
                break;
            case 4:
                str = getFileName( str, name );
                ofpg = initFILE( name, "w+" );
                break;
            default:
                p = name;
                while( *str != '\0' ) {
                    if( myisspace( *str ) )
                        break;
                    *p++ = *str++;
                }
                *p = '\0';
                if( *name != '\0' ) {
                    if( (mask = findTarget( name )) == 0 ) {
                        fail( "invalid target name '%s'\n", name );
                    }
                    targetMask |= mask;
                }
                break;
            }
        }
    }
    free( fstr );
    return( rc );
}

#define NUM_FILES   5

int main( int argc, char **argv )
{
    bool    ok;
    int     i;

    setlocale( LC_ALL, "C" );
    ok = true;
    initTargets();
    initOptions();
    for( i = 1; i < argc; i++ ) {
        if( ProcessOptions( argv[i] ) ) {
            ok = false;
            break;
        }
    }
    if( !ok
      || fno < NUM_FILES ) {
        ok = false;
        dumpUsage();
    } else {
        initUTF8();
        initUsageGroup();
        readInputFile();
        assignCodeChainToOptions();
        assignUsageChainToOptions();
        checkForMissingUsages();
        stripUselessOptions();
        initOptionFields();

        if( errcount > 0 ) {
            ok = false;
        } else {
            outputInit();
            startParserH();
            outputFN_PROCESS();
            if( !optFlag.parser_only ) {
                outputFN_INIT();
                outputFN_FINI();
            }
            finishParserH();
            outputUsageH();
            dumpInternational();
            outputFini();
        }
    }
    closeFiles();
    finiTargets();
    if( ok )
        return( EXIT_SUCCESS );
    return( EXIT_FAILURE );
}
